def gather_existing_markdown_context(project_dir: Path, max_files: int = 20) -> list[dict]:
    generated_roots = {
            (
                "AGENTS.md",
                "AI_DOCS_INDEX.md",
                "CLAUDE.md",
                "CODEX.md",
                "ANTIGRAVITY.md",
            )
        }
    docs = []
    for md_file in sorted(project_dir.rglob("*.md")):
        rel = md_file.relative_to(project_dir).as_posix()
        if rel.startswith(".git/"):
            continue
        if rel.startswith(".ai-docs/"):
            continue
        if rel.startswith(".github/") or rel.startswith(".cursor/"):
            continue
        if rel in generated_roots:
            continue
        try:
            content = md_file.read_text(encoding="utf-8", errors="ignore")
        except Exception:
            continue
        docs.append({{"path": rel, "preview": content[:500].strip()}})
        if len(docs) >= max_files:
            break
    return docs


def merge_unique(existing: list[str], incoming: list[str]) -> list[str]:
    merged = []
    seen = set()
    for item in (existing or []) + (incoming or []):
        value = item.strip()
        if not value:
            continue
        key = value.lower()
        if key in seen:
            continue
        seen.add(key)
        merged.append(value)
    return merged


