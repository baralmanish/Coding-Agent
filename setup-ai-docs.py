def hash_text(content: str) -> str:
    return hashlib.sha256(content.encode("utf-8")).hexdigest()


def detect_project_type(project_dir: Path) -> str:
    manifest_candidates = [
        "package.json",
        "pyproject.toml",
        "requirements.txt",
        "go.mod",
        "Cargo.toml",
        "pom.xml",
        "build.gradle",
        "Gemfile",
    ]
    has_manifest = any((project_dir / name).exists() for name in manifest_candidates)
    has_source = any((project_dir / d).exists() for d in ["src", "app", "services", "backend", "frontend"])
    has_docs = any(project_dir.glob("*.md"))
    return "existing" if (has_manifest or has_source or has_docs) else "new"


