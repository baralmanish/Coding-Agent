CUSTOM_BLOCK_RE = re.compile(
    r"<!--\\s*user-custom:start(?::(?P<id>[A-Za-z0-9_.-]+))?\\s*-->.*?<!--\\s*user-custom:end(?:[:][A-Za-z0-9_.-]+)?\\s*-->",
    re.DOTALL,
)


def extract_custom_blocks(content: str) -> dict[str, str]:
    blocks = {{}}
    index = 0
    for match in CUSTOM_BLOCK_RE.finditer(content or ""):
        index += 1
        block = match.group(0)
        block_id = match.group("id") or f"block_{{index}}"
        blocks[block_id] = block
    return blocks


def ensure_default_custom_block(content: str) -> str:
    if "<!-- user-custom:start" in content:
        return content
    return (
        content.rstrip()
        + "\\n\\n<!-- user-custom:start:local -->\\n"
        + "# Add project-specific guidance here.\\n"
        + "<!-- user-custom:end:local -->\\n"
    )


def preserve_user_custom_blocks(existing_content: str, new_content: str) -> str:
    merged = ensure_default_custom_block(new_content)
    existing_blocks = extract_custom_blocks(existing_content)
    new_blocks = extract_custom_blocks(merged)

    preserved_unmatched = []
    for block_id, existing_block in existing_blocks.items():
        if block_id in new_blocks:
            merged = merged.replace(new_blocks[block_id], existing_block, 1)
        else:
            preserved_unmatched.append(existing_block)

    if preserved_unmatched:
        merged = merged.rstrip() + "\\n\\n## Preserved User Customizations\\n\\n" + "\\n\\n".join(preserved_unmatched) + "\\n"

    return merged


def resolve_generated_at(project_dir: Path, check_mode: bool) -> str:
    if not check_mode:
        return now_utc()

    metadata_path = project_dir / ".ai-docs" / "metadata.json"
    if metadata_path.exists():
        try:
            metadata = json.loads(metadata_path.read_text(encoding="utf-8", errors="ignore"))
            value = metadata.get("generated_at")
            if isinstance(value, str) and value.strip():
                return value.strip()
        except Exception:
            pass

    return now_utc()


